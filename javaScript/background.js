//function to create scrolling background
function handleBackground() {

    if (BG.x1 <= -BG.width + gameSpeed) BG.x1 = BG.width - 3;
    else BG.x1 -= gameSpeed;
    if (BG.x2 <= -BG.width + gameSpeed) BG.x2 = BG.width - 3;
    else BG.x2 -= gameSpeed;

    if (currentBackground === 'pinkForest' ){drawPinkForestBackground()};
    if (currentBackground === 'beachy'){drawBeachyBackground()}
    if (currentBackground === 'shroomForest'){drawShroomForestBackground()};
    if (currentBackground === 'dreary'){ drawDrearyBackground()}
    if (currentBackground === 'pinkAndSweet'){drawPinkAndSweetBackground()}
};

